    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        if(textid .getText().equals("")) {
            JOptionPane.showMessageDialog(this, "All fields are required");
            textid.requestFocus();
        }
        else if(textname .getText().equals("")) {
            JOptionPane.showMessageDialog(this, "All fields are required");
            textname.requestFocus();
        }
        else if(textpublisher .getText().equals("")) {
            JOptionPane.showMessageDialog(this, "All fields are required");
            textpublisher.requestFocus();
        }
        else if(textprice .getText().equals("")) {
            JOptionPane.showMessageDialog(this, "All fields are required");
            textprice.requestFocus();
        }
       else if(textyear .getText().equals("")) {
            JOptionPane.showMessageDialog(this, "All fields are required");
            textyear.requestFocus();
        }
       else {
            try {
                Connection con = Connect.Connection();
                PreparedStatement ps= con.prepareStatement("insert into book(id,name,publisher,price,year,status) values(?,?,?,?,?,?)");
                ps.setString(1, textid.getText());
                ps.setString(2, textname.getText());
                ps.setString(3, textpublisher.getText());
                ps.setString(4, textprice.getText());
                ps.setString(5, textyear.getText());
                ps.setString(6, "Not-Issued");
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Record Saved");
                textid.setText("");
                textname.setText("");
                textpublisher .setText("");
                textprice.setText("");
                textyear.setText("");
                
                
            } catch (SQLException ex) {
                System.getLogger(Addbook.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }
       }
    }//GEN-LAST:event_btnsaveActionPerformed

