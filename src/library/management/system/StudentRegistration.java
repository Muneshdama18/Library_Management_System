    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        if(textid.getText().equals("")){
            JOptionPane.showMessageDialog(this,"All Fields are required");
            textid.requestFocus();
        }
        else if(textname.getText().equals("")){
            JOptionPane.showMessageDialog(this,"All Fields are required");
            textname.requestFocus();
        }
        else if(textcourse.getText().equals("")){
            JOptionPane.showMessageDialog(this,"All Fields are required");
            textcourse.requestFocus();
        }
        else if(textbranch.getText().equals("")){
            JOptionPane.showMessageDialog(this,"All Fields are required");
            textbranch.requestFocus();
        }
        else if(textsemester.getText().equals("")){
            JOptionPane.showMessageDialog(this,"All Fields are required");
            textsemester.requestFocus();
        }
        else {
            try {
                Connection con = Connect.Connection();
                PreparedStatement pst = con.prepareStatement("insert into `library`. `student`(id,name,course,branch,semester)values(?,?,?,?,?)");
                pst.setString(1, textid.getText());
                pst.setString(2, textname.getText());
                pst.setString(3, textcourse.getText());
               pst.setString(4, textbranch.getText());
               pst.setString(5, textsemester.getText());
               pst.executeUpdate();
               JOptionPane.showMessageDialog(this, "Record saved");
               textid.setText("");
                textname.setText("");
               textcourse.setText("");
              textbranch.setText("");
                 textsemester.setText("");
            } catch (SQLException ex) {
                System.getLogger(StudentRegistration.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }
            
        }
    }//GEN-LAST:event_btnsaveActionPerformed

