    private void btnissueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnissueActionPerformed
        // TODO add your handling code here:
        
         if(textbookid.getText().equals("")) {
        JOptionPane.showMessageDialog(this,"Please enter Book Id and search it");
        textbookid.requestFocus();
    }
         else {
             try {
                 ps = con.prepareStatement("update book set status =?, issuedate=?,duedate =?,studentid =?");
                 ps.setString(1,"ISSUED");
                 ps.setString(2,textissuedate.getText());
                 ps.setString(3,textduedate.getText());
                 ps.setString(4,textstudentid.getText());
           ps.executeUpdate();
           JOptionPane.showMessageDialog(this,"Book Issued");
           textbookid.setText("");
            textbookname.setText("");
            textissuedate.setText("");
            textduedate.setText("");
            textstudentid.setText("");
           
             } catch (SQLException ex) {
                 System.getLogger(Issuebook.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
             }
         }
        
        
    }//GEN-LAST:event_btnissueActionPerformed

    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        if(textbookid.getText().equals("")) {
        JOptionPane.showMessageDialog(this,"Please enter Book Id and search it");
        textbookid.requestFocus();
    }
        else {
            try {
                ps = con.prepareStatement("select * from book where id = ?");
                ps.setString(1,textbookid.getText());
                rs = ps.executeQuery();
                if(rs.next()) {
                    textbookname.setText(rs.getString(2));
                }
                
            } catch (SQLException ex) {
                System.getLogger(Issuebook.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }
        }
        
        
        
        
    }//GEN-LAST:event_btnsearchActionPerformed

