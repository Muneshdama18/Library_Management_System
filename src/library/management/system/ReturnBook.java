    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        if(textstudentid.getText().equals("")) {
            JOptionPane.showMessageDialog(this,"Please Enter Student ID and search it");
            textstudentid.requestFocus();
        }
        else {
            try {
                ps = con.prepareStatement("select student.id,student.name,book.id,book.name,book.issuedate,book.duedate from book inner join student using(id) where book.id = ?");
                ps.setString(1, textstudentid.getText());
                rs = ps.executeQuery();
                if(rs.next()) {
                    textbookid.setText(rs.getString(2));
                      textstudentname.setText(rs.getString(1));
                        textduedate.setText(rs.getString(5));
                         textbookname.setText(rs.getString(3));
                     
                        textissuedate.setText(rs.getString(4));
                }
                else {
                    JOptionPane.showMessageDialog(this,"Please Enter valid Student ID");
                }
            } catch (SQLException ex) {
                System.getLogger(ReturnBook.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }
        }
    }//GEN-LAST:event_btnsearchActionPerformed

